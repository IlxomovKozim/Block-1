package diff21;

public class Diff21 {
    public int diff21(int n) { 
    
        return Math.abs(21-n);
    } 
    public static void main(String[] args) {
        
        Diff21 obj = new Diff21();
        
    System.out.println(obj.diff21(19)); 
    System.out.println(obj.diff21(10)); 
    System.out.println(obj.diff21(21)); 
    
    }  
}
